/* Tab48426 for unvoiced consonant values */
static const uint8_t tab48426[5] = {0x18, 0x1a, 0x17, 0x17, 0x17};

/* Sample table (1280 bytes) - included from external file for readability */
#include "sam_sample_table.h"

/* ============================================================
 * Enhancement flags (passed as 11th arg)
 * ============================================================ */
#define FLAG_GLOTTAL_LF   0x01  /* Use LF glottal pulse instead of sinus */
#define FLAG_ASPIRATION   0x02  /* Add aspiration noise to voiced sounds */
#define FLAG_BANDWIDTH    0x04  /* Widen formant bandwidth (softer resonance) */
#define FLAG_INTERPOLATE  0x08  /* Formant frequency interpolation */

/* ============================================================
 * Inline output: write to buffer via timetable
 * ============================================================ */
static inline void output(uint8_t *buf, uint32_t bufsize,
                          uint32_t *bufpos, uint8_t *old_ti,
                          uint8_t index, uint8_t a) {
    *bufpos += timetable[*old_ti][index];
    *old_ti = index;
    uint32_t p = *bufpos / 50;
    if (p + 4 < bufsize) {
        uint8_t v = (a & 15) * 16;
        buf[p] = v; buf[p+1] = v; buf[p+2] = v; buf[p+3] = v; buf[p+4] = v;
    }
}

/* ============================================================
 * render_sample: sampled consonant output
 * ============================================================ */
static void render_sample(uint8_t *buf, uint32_t bufsize,
                          uint32_t *bufpos, uint8_t *old_ti,
                          uint8_t *m_off, uint8_t consonant_flag,
                          const uint8_t *pitches_arr, uint8_t y) {
    uint8_t hibyte = ((consonant_flag & 7) - 1) & 0xFF;
    uint32_t hi = (uint32_t)hibyte * 256;
    uint8_t pitchl = consonant_flag & 248;

    if (pitchl == 0) {
        /* Voiced */
        uint8_t off = *m_off;
        uint8_t vph = ((pitches_arr[y] >> 4) ^ 255) & 0xFF;
        do {
            uint8_t sample = (hi + off < sizeof(sample_table)) ? sample_table[hi + off] : 0;
            for (int bit = 0; bit < 8; bit++) {
                if (sample & 128) {
                    output(buf, bufsize, bufpos, old_ti, 3, 26);
                } else {
                    output(buf, bufsize, bufpos, old_ti, 4, 6);
                }
                sample = (sample << 1) & 0xFF;
            }
            off = (off + 1) & 0xFF;
            vph = (vph + 1) & 0xFF;
        } while (vph != 0);
        *m_off = off;
    } else {
        /* Unvoiced */
        uint8_t off = (pitchl ^ 255) & 0xFF;
        uint8_t m_val = (hibyte < sizeof(tab48426)) ? tab48426[hibyte] : 0x18;
        do {
            uint8_t sample = (hi + off < sizeof(sample_table)) ? sample_table[hi + off] : 0;
            for (int bit = 0; bit < 8; bit++) {
                if (sample & 128) {
                    output(buf, bufsize, bufpos, old_ti, 2, 5);
                } else {
                    output(buf, bufsize, bufpos, old_ti, 1, m_val);
                }
                sample = (sample << 1) & 0xFF;
            }
            off = (off + 1) & 0xFF;
        } while (off != 0);
    }
}

/* ============================================================
 * process_frames: main synthesis function
 *
 * Python: sam_render.process_frames(
 *     freq1, freq2, freq3, amp1, amp2, amp3,
 *     pitches, samp_flags, num_frames, speed[, flags])
 * Returns: bytearray (22050 Hz via timetable)
 *
 * flags (optional, default 0):
 *   bit 0 (0x01): Use LF glottal pulse
 *   bit 1 (0x02): Aspiration noise
 *   bit 2 (0x04): Formant bandwidth widening
 *   bit 3 (0x08): Formant frequency interpolation
 * ============================================================ */
static mp_obj_t process_frames(size_t n_args, const mp_obj_t *args) {
    /* Extract buffer pointers */
    mp_buffer_info_t b_fr1, b_fr2, b_fr3, b_am1, b_am2, b_am3, b_pit, b_sf;
    mp_get_buffer_raise(args[0], &b_fr1, MP_BUFFER_READ);
    mp_get_buffer_raise(args[1], &b_fr2, MP_BUFFER_READ);
    mp_get_buffer_raise(args[2], &b_fr3, MP_BUFFER_READ);
    mp_get_buffer_raise(args[3], &b_am1, MP_BUFFER_READ);
    mp_get_buffer_raise(args[4], &b_am2, MP_BUFFER_READ);
    mp_get_buffer_raise(args[5], &b_am3, MP_BUFFER_READ);
    mp_get_buffer_raise(args[6], &b_pit, MP_BUFFER_READ);
    mp_get_buffer_raise(args[7], &b_sf,  MP_BUFFER_READ);

    uint32_t num_frames = mp_obj_get_int(args[8]);
    uint32_t speed = mp_obj_get_int(args[9]);

    /* Enhancement flags (optional 11th arg, default 0 = original SAM) */
    uint32_t enh_flags = 0;
    if (n_args > 10) {
        enh_flags = mp_obj_get_int(args[10]);
    }

    const uint8_t *fr1 = (const uint8_t *)b_fr1.buf;
    const uint8_t *fr2 = (const uint8_t *)b_fr2.buf;
    const uint8_t *fr3 = (const uint8_t *)b_fr3.buf;
    const uint8_t *am1 = (const uint8_t *)b_am1.buf;
    const uint8_t *am2 = (const uint8_t *)b_am2.buf;
    const uint8_t *am3 = (const uint8_t *)b_am3.buf;
    const uint8_t *pitches = (const uint8_t *)b_pit.buf;
    const uint8_t *samp_flags = (const uint8_t *)b_sf.buf;

    /* Select voicing waveform table */
    const uint8_t *sinus = (enh_flags & FLAG_GLOTTAL_LF) ? glottal_lf : sinus_original;

    /* Allocate output buffer (22050 Hz via timetable) */
    uint32_t bufsize = 4 * speed * num_frames + 4096;
    uint8_t *buf = m_new(uint8_t, bufsize);
    memset(buf, 0, bufsize);

    /* Synthesis state */
    uint32_t bufpos = 0;
    uint8_t old_ti = 0;
    uint8_t phase1 = 0, phase2 = 0, phase3 = 0;
    uint8_t m_off = 0;
    uint8_t y = 0;
    uint8_t k = (uint8_t)num_frames;
    uint8_t speedcounter = (uint8_t)speed;

    uint8_t glottal_pulse = pitches[0];
    uint8_t n = glottal_pulse - (glottal_pulse >> 2);

    /* Aspiration noise PRNG state (LCG) */
    uint32_t lcg_state = 0xDEADBEEF;

    /* Formant interpolation state: previous frame's frequencies */
    uint8_t prev_fr1 = fr1[0], prev_fr2 = fr2[0], prev_fr3 = fr3[0];
    uint8_t interp_counter = 0;  /* counts up within each frame */
    uint8_t interp_limit = (uint8_t)speed;  /* interpolate over one frame (speed samples) */

    /* Main synthesis loop */
    while (k) {
        uint8_t flags = (y < num_frames) ? samp_flags[y] : 0;

        if (flags & 248) {
            /* Sampled consonant */
            render_sample(buf, bufsize, &bufpos, &old_ti, &m_off,
                         flags, pitches, y);
            y = (y + 2) & 0xFF;
            k = (k - 2) & 0xFF;
            speedcounter = (uint8_t)speed;
            /* Reset interpolation state for next frame */
            if (y < num_frames) {
                prev_fr1 = fr1[y]; prev_fr2 = fr2[y]; prev_fr3 = fr3[y];
            }
            interp_counter = 0;
        } else {
            /* === Enhancement 1 & 2: Formant interpolation === */
            uint8_t cur_f1, cur_f2, cur_f3;
            if ((enh_flags & FLAG_INTERPOLATE) && interp_limit > 0) {
                /* Linear interpolation from prev_frN to fr[y] over speed samples */
                uint8_t t = interp_counter;
                uint8_t total = interp_limit;
                /* Interpolate: prev + (cur - prev) * t / total */
                cur_f1 = prev_fr1 + (((int16_t)fr1[y] - (int16_t)prev_fr1) * t) / total;
                cur_f2 = prev_fr2 + (((int16_t)fr2[y] - (int16_t)prev_fr2) * t) / total;
                cur_f3 = prev_fr3 + (((int16_t)fr3[y] - (int16_t)prev_fr3) * t) / total;
            } else {
                cur_f1 = fr1[y]; cur_f2 = fr2[y]; cur_f3 = fr3[y];
            }

            /* Formant synthesis via mult_table */
            uint8_t tmp = mult_table[sinus[phase1] | am1[y]];
            tmp += mult_table[sinus[phase2] | am2[y]];
            if (tmp > 255) tmp += 1;  /* uint8_t overflow matching original */
            tmp += mult_table[rectangle[phase3] | am3[y]];

            /* === Enhancement 5: Formant bandwidth widening === */
            if (enh_flags & FLAG_BANDWIDTH) {
                /* Blend formant output toward center (softer resonance)
                 * tmp = (tmp * 13 + 128 * 3) >> 4 */
                tmp = (uint8_t)(((uint16_t)tmp * 13 + 384) >> 4);
            }

            tmp += 136;
            tmp >>= 4;

            /* === Enhancement 3: Aspiration noise === */
            if ((enh_flags & FLAG_ASPIRATION) && am1[y] > 0) {
                uint8_t noise = (lcg_state >> 16) & 0x0F;
                lcg_state = lcg_state * 1664525 + 1013904223;
                /* ~6% noise mix: tmp = (tmp * 14 + noise) >> 4 */
                tmp = (uint8_t)(((uint16_t)(tmp & 0x0F) * 14 + noise) >> 4);
            }

            output(buf, bufsize, &bufpos, &old_ti, 0, tmp & 0x0F);

            interp_counter++;
            speedcounter = (speedcounter - 1) & 0xFF;
            if (speedcounter == 0) {
                /* Frame boundary: save current frequencies as previous */
                prev_fr1 = fr1[y]; prev_fr2 = fr2[y]; prev_fr3 = fr3[y];
                interp_counter = 0;

                y = (y + 1) & 0xFF;
                k = (k - 1) & 0xFF;
                if (k == 0) break;
                speedcounter = (uint8_t)speed;
            }

            glottal_pulse = (glottal_pulse - 1) & 0xFF;
            if (glottal_pulse != 0) {
                n = (n - 1) & 0xFF;
                if ((n != 0) || (flags == 0)) {
                    phase1 = (phase1 + cur_f1) & 0xFF;
                    phase2 = (phase2 + cur_f2) & 0xFF;
                    phase3 = (phase3 + cur_f3) & 0xFF;
                    continue;
                }
                /* Voiced sample interleave */
                render_sample(buf, bufsize, &bufpos, &old_ti, &m_off,
                             flags, pitches, y);
            }
        }

        glottal_pulse = pitches[y];
        n = glottal_pulse - (glottal_pulse >> 2);
        phase1 = 0; phase2 = 0; phase3 = 0;
    }

    /* Return full 22050 Hz buffer (no downsampling) */
    uint32_t end_22k = bufpos / 50;
    /* Shrink buffer to actual used size */
    uint8_t *out = m_new(uint8_t, end_22k);
    memcpy(out, buf, end_22k);
    m_free(buf);

    return mp_obj_new_bytearray_by_ref(end_22k, out);
}
